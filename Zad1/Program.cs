using System;

namespace Zad1
{
    class Program
    {
        static void Main(string[] args)
        {
            // Создаем объекты класса Person
            Person человек1 = new Person();
            Person человек2 = new Person();

            // Устанавливаем значения полей для человека 1
            человек1.УстановитьИмя("Иван");
            человек1.УстановитьВозраст(30);
            человек1.УстановитьАдрес("ул. Главная, 123");

            // Устанавливаем значения полей для человека 2
            человек2.УстановитьИмя("Анна");
            человек2.УстановитьВозраст(25);
            человек2.УстановитьАдрес("ул. Вязов, 456");

            // Выводим информацию о каждом человеке
            Console.WriteLine("Человек 1:");
            Console.WriteLine("Имя: " + человек1.ПолучитьИмя());
            Console.WriteLine("Возраст: " + человек1.ПолучитьВозраст());
            Console.WriteLine("Адрес: " + человек1.ПолучитьАдрес());

            Console.WriteLine();

            Console.WriteLine("Человек 2:");
            Console.WriteLine("Имя: " + человек2.ПолучитьИмя());
            Console.WriteLine("Возраст: " + человек2.ПолучитьВозраст());
            Console.WriteLine("Адрес: " + человек2.ПолучитьАдрес());
        }
    }

    class Person
    {
        private string имя;
        private int возраст;
        private string адрес;

        // Метод для установки имени
        public void УстановитьИмя(string новоеИмя)
        {
            имя = новоеИмя;
        }

        // Метод для получения имени
        public string ПолучитьИмя()
        {
            return имя;
        }

        // Метод для установки возраста
        public void УстановитьВозраст(int новыйВозраст)
        {
            возраст = новыйВозраст;
        }

        // Метод для получения возраста
        public int ПолучитьВозраст()
        {
            return возраст;
        }

        // Метод для установки адреса
        public void УстановитьАдрес(string новыйАдрес)
        {
            адрес = новыйАдрес;
        }

        // Метод для получения адреса
        public string ПолучитьАдрес()
        {
            return адрес;
        }
    }
}
